function Icon({children, color = 'var(--text-dark)', ...props}) {
  const ref = React.useRef();
  
  React.useEffect(() => {
    const ctx = gsap.context(() => {
      const q = gsap.utils.selector( ref.current );
      
      gsap.set( q('path'), {
        fill: color
      });

    }, ref.current);

    return () => ctx.revert();
  }, []);

  return (
    <svg ref={ref} {...props}>
      { children }
    </svg>
  )
}